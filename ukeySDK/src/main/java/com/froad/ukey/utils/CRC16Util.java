    /**
     * 计算CRC16校验  对外的接口
     *
     * @param data 需要计算的数组
     * @return CRC16校验值
     */
    public static int calcCrc16(byte[] data) {
        return calcCrc16(data, 0, data.length);
    }

    /**
     * 计算CRC16校验
     *
     * @param data   需要计算的数组
     * @param offset 起始位置
     * @param len    长度
     * @return CRC16校验值
     */
    public static int calcCrc16(byte[] data, int offset, int len) {
        return calcCrc16(data, offset, len, 0xffff);
    }

    /**
     * 计算CRC16校验
     *
     * @param data   需要计算的数组
     * @param offset 起始位置
     * @param len    长度
     * @param preval 之前的校验值
     * @return CRC16校验值
     */
    public static int calcCrc16(byte[] data, int offset, int len, int preval) {
        int ucCRCHi = (preval & 0xff00) >> 8;
        int ucCRCLo = preval & 0x00ff;
        int iIndex;
        for (int i = 0; i < len; ++i) {
            iIndex = (ucCRCLo ^ data[offset + i]) & 0x00ff;
            ucCRCLo = ucCRCHi ^ crc16_tab_h[iIndex];
            ucCRCHi = crc16_tab_l[iIndex];
        }
        return ((ucCRCHi & 0x00ff) << 8) | (ucCRCLo & 0x00ff) & 0xffff;
    }

    public static byte[] getCrc16(byte[] arr_buff) {
        int len = arr_buff.length;

        // 预置 1 个 16 位的寄存器为十六进制FFFF, 称此寄存器为 CRC寄存器。
        int crc = 0xFFFF;
        int i, j;
        for (i = 0; i < len; i++) {
            // 把第一个 8 位二进制数据 与 16 位的 CRC寄存器的低 8 位相异或, 把结果放于 CRC寄存器
            crc = ((crc & 0xFF00) | (crc & 0x00FF) ^ (arr_buff[i] & 0xFF));
            for (j = 0; j < 8; j++) {
                // 把 CRC 寄存器的内容右移一位( 朝低位)用 0 填补最高位, 并检查右移后的移出位
                if ((crc & 0x0001) > 0) {
                    // 如果移出位为 1, CRC寄存器与多项式A001进行异或
                    crc = crc >> 1;
                    crc = crc ^ 0xA001;
                } else
                    // 如果移出位为 0,再次右移一位
                    crc = crc >> 1;
            }
        }
        return intToBytes(crc);
    }

    /**
     * 将int转换成byte数组，低位在前，高位在后
     * 改变高低位顺序只需调换数组序号
     */
    private static byte[] intToBytes(int value) {
        byte[] src = new byte[2];
        src[1] = (byte) ((value >> 8) & 0xFF);
        src[0] = (byte) (value & 0xFF);
        return src;
    }

    /**
     * 将8位倒置
     * @param t
     * @return
     */
    private static byte reverseByte (byte t) {
        String ts = Integer.toBinaryString(t & 0x000000FF);
        int tsLen = ts.length();
        StringBuffer sbf = new StringBuffer();
        if (tsLen >= 8) {
            sbf.append(ts.substring(tsLen - 8));
        } else {
            for (int i = 0; i < 8 - tsLen; i ++) {
                sbf.append("0");
            }
            sbf.append(ts);
        }
        sbf = sbf.reverse();
        ts = sbf.toString();
        byte tb = (byte) (Integer.parseInt(ts, 2) & 0x000000FF);
        return tb;
    }

    /**
     * 获取CRC16校验结果
     * @param data
     * @return
     */
    public static byte[] getCrc16Key (byte[] data) {
        int crcResult = 0;
        int dataLen = data.length;
        int t1 = 0;
        int t2 = 0;

        for (int i = 0; i < dataLen; i ++) {
            t1 = data[i];
            t2 = reverseByte((byte)(t1 & 0x000000FF));
            for (int k = 0x80; k != 0; k = k >> 1) {
                if((crcResult & 0x00008000) != 0)
                {
                    crcResult = crcResult << 1;
                    crcResult = crcResult ^ 0x00001021; //CRC-16:0x8005  ; CCITT:0x1021
                }
                else
                {
                    crcResult = crcResult << 1;
                }
                crcResult = crcResult & 0x0000FFFF;

                if((t2 & 0x000000FF & k)!=0)
                {
                    crcResult = crcResult ^ 0x00001021; //CRC-16:0x8005  ; CCITT:0x1021
                }
            }
        }
        TMKeyLog.d(TAG, "getCrc16Temp>>>crcResult---111:" + crcResult);
        t1 = (crcResult & 0x0000FFFF) >> 8;
        TMKeyLog.d(TAG, "getCrc16Temp>>>t1---111:" + t1);
        t2 = reverseByte((byte)(t1 & 0x000000FF));
        TMKeyLog.d(TAG, "getCrc16Temp>>>t2---111:" + t2);

        crcResult = crcResult & 0x000000FF;
        TMKeyLog.d(TAG, "getCrc16Temp>>>crcResult---222:" + crcResult);
        crcResult = crcResult | ((t2 & 0x000000FF) << 8);
        TMKeyLog.d(TAG, "getCrc16Temp>>>crcResult---333:" + crcResult);

        t1 = crcResult & 0x000000FF; //
        TMKeyLog.d(TAG, "getCrc16Temp>>>t1---222:" + t1);
        t2 = reverseByte((byte)(t1 & 0x000000FF));
        TMKeyLog.d(TAG, "getCrc16Temp>>>t2---222:" + t2);

        crcResult = crcResult & 0x0000FF00;
        TMKeyLog.d(TAG, "getCrc16Temp>>>crcResult---444:" + crcResult);
        crcResult = crcResult | (t2 & 0x000000FF);
        TMKeyLog.d(TAG, "getCrc16Temp>>>crcResult---555:" + crcResult);

        String hs = Integer.toHexString(crcResult);
        int hsLen = hs.length();
        TMKeyLog.d(TAG, "getCrc16Temp>>>hsLen:" + hsLen);
        if (hsLen >= 4) {
            hs = hs.substring(hsLen - 4);
            TMKeyLog.d(TAG, "getCrc16Temp>>>hs:" + hs);
            return FCharUtils.hexString2ByteArray(hs);
        }
        StringBuffer sbf = new StringBuffer();
        for (int i = 0; i < 4 - hsLen; i ++) {
            sbf.append("0");
        }
        sbf.append(hs);
        hs = sbf.toString();
        TMKeyLog.d(TAG, "getCrc16Temp>>>hs:" + hs);
        return FCharUtils.hexString2ByteArray(hs);
    }
